from typing import *


class Solution:
    def exist(self, board: List[List[str]], word: str) -> bool:
        for row_idx in range(len(board)):
            for col_idx in range(len(board[row_idx])):
                if self.recursion(board, row_idx, col_idx, word, 0):
                    return True
        return False

    def recursion(self, board, row_idx, col_idx, word, word_idx):
        if row_idx >= len(board) or row_idx < 0 or col_idx >= len(board[row_idx]) or col_idx < 0:
            return False
        if board[row_idx][col_idx] != word[word_idx]:
            return False
        if word_idx == len(word) - 1:
            return True
        temp = board[row_idx][col_idx]
        board[row_idx][col_idx] = '#'
        found = self.recursion(board, row_idx+1, col_idx, word, word_idx+1) or \
                self.recursion(board, row_idx-1, col_idx, word, word_idx+1) or \
                self.recursion(board, row_idx, col_idx+1, word, word_idx+1) or \
                self.recursion(board, row_idx, col_idx-1, word, word_idx+1)
        board[row_idx][col_idx] = temp
        return found

